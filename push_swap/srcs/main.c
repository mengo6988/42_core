#include "push_swap.h"

int	main(int ac, char **av)
{
	int		i;
	char	**res;
	int		*arr;

	i = 0;
	if (ac < 2)
		return (0);
	while (av[++i])
		res = ft_split(av[i], ' ');
	i = -1;
	while (res[++i])
		arr[i] = ft_atoi(res[i]);
}
