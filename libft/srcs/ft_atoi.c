int	ft_atoi(const char *s)
{
	int	i;
	int	is_pos;
	int	res;

	i = 0;
	is_pos = 1;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i++] == '-')
			is_pos *= -1;
	}
	res = 0;
	while (s[i] && (s[i] >= '0' && s[i] <= '9'))
	{
		res *= 10;
		res += (s[i] - '0') * is_pos;
		i++;
	}
	return (res);
}
