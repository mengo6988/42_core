#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*dest;
	int		i;
	int		j;

	dest = (char *)malloc(sizeof(s1) + sizeof(s2) + 1);
	if (!dest)
		return (0);
	i = 0;
	while (s1[i])
		dest[i] = s1[i++];
	j = 0;
	while (s2[j])
		dest[i++] = s2[j++];
	dest[i] = '\0';
	return (dest);
}
