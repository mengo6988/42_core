#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	int		i;

	i = ft_strlen(s);
	p = s + i;
	while (*p != *s && *p != c)
	{
		if (*p == c)
			return (p);
		p--;
	}
	return (NULL);
}
